public class ArithmeticMeanOfThreeNumbersInArray {

    public static int arithmeticMeanOfThreeNumbers(int [] array){

        System.out.println("Среднее значение из трех случайных чисел массива: ");

        return (array[0] + array[1] + array[2]) / 3;
    }
}
