public class EqualityOfNumbers {

    public static void equalityOfNumbers (int left, int right){

        if (left == right){

            System.out.println("Числа равны: " + left + " == " + right);

        } else {

            System.out.println("Числа разные: " + left + " != " + right);

        }
    }
}
